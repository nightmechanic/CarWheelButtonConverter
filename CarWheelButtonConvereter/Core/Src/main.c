	static uint8_t revCount = 0;
	static uint8_t doMute = 0;
	static uint8_t doFwd = 0;
	static uint8_t doRev = 0;


	uint8_t index;


	const uint16_t thresholds1[] = {362, 651, 1016, 1489, 2129, 2400, 2969, 5000};
	const uint16_t thresholds2[] = {362, 651, 1016, 1489, 2129, 2400, 2969, 5000};
	//PWM values num of cycles @ timer clock, (4MHz), period is 250 cycles
	const uint8_t pwmValues1[16]= {240, 55, 30, 16, 8, 8, 2, 0, 0, 0, 0};
	const uint8_t pwmValues2[16]= {240, 55, 30, 16, 8, 8, 2, 0, 0, 0, 0};
	//Simulation based : {60, 44, 30, 22, 16, 10, 4, 0, 0}



  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
